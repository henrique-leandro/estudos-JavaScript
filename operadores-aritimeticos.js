// OPERADORES ARITIMÉTICOS


/*
  + -> adição
  - -> subtração
  * -> multiplicação
  / -> divição
  % -> resto
  ++ -> incremento
  -- -> decremento
  ** -> Exponencial


*/

let adição = 20 + 20;
let subtração = 20 - 20;
let multiplicação = 20 * 20;
let divição = 20 / 2;
let resto = 7 % 3;

let incremento = 5;
incremento++
console.log(incremento);

let decremento = 5;
decremento--
console.log(decremento);

let exponencial = 5 ** 2; // multiplicar o 5 duas vezes por ele mesmo
console.log(exponencial); 
