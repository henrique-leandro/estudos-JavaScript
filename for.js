/*
  Estrutura de repetição - FOR

  dentro do for temos 3 parametros
  - uma variavel com valor inicial
  - uma condicional ex: enquanto a variavel for menor que 10 : i < 10
  - 

*/

let nome = "henrique"

for(let i = 0; i < 200; i++){
    console.log(`${nome} ` + i)
}



let tabuada = 5

for (let i = 0; i <= 10; i++){
    console.log(`${tabuada} x ${i} = ${tabuada * i} `)
}